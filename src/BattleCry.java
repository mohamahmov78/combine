public abstract class BattleCry {
    private String battleCryName;
    private String BattleCryDetails = null;
    public String getBattleCryName () {return battleCryName;}
    public void setBattleCryName (String name) {this.battleCryName = name;}
    public String getBattleCryDetails() {return BattleCryDetails;}
    public void  setBattleCryDetails (String battleCryDetails) {BattleCryDetails = battleCryDetails;}
}
